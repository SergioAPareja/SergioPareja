#define servo 14
#define button 21
#define Bit1 27
#define Bit2 33
#define Bit3 15
#define Bit4 32

bool direction = true; //1 is up, 0 is down
int state = 16;
bool toggle[4] = {1,1,1,1};
bool buttonPressed = false;


static void LED(){
    /* Configure the IOMUX register for pad BLINK_GPIO (some pads are
       muxed to GPIO on reset already, but some default to other
       functions and need to be switched to GPIO. Consult the
       Technical Reference for a list of pads and their default
       functions.)
    */
    gpio_pad_select_gpio(Bit1);
    gpio_set_direction(Bit1, GPIO_MODE_OUTPUT);
    gpio_pad_select_gpio(Bit2);
    gpio_set_direction(Bit2, GPIO_MODE_OUTPUT);
    gpio_pad_select_gpio(Bit3);
    gpio_set_direction(Bit3, GPIO_MODE_OUTPUT);
    gpio_pad_select_gpio(Bit4);
    gpio_set_direction(Bit4, GPIO_MODE_OUTPUT);



    while(1) {

        vTaskDelay(1000 / portTICK_PERIOD_MS);
        gpio_set_level(Bit1, toggle[0]);
        gpio_set_level(Bit2, toggle[1]);
        gpio_set_level(Bit3, toggle[2]);
        gpio_set_level(Bit4, toggle[3]);

    if(state%2 == 0)
    {
        toggle[0] = !toggle[0];
    }

    if(state%4 == 0)
        toggle[1] = !toggle[1];

    if(state%8 == 0)
        toggle[2] = !toggle[2];


    if(state%16 == 0)
        toggle[3] = !toggle[3];

    printf("Direction is: %d\n", direction);

    if(direction)
        state= state + 1;
    else
        state = state -1;
    if(state == 48 && direction == 1)
        state = 16;
    if(state == 16 && direction == 0)
        state = 48;
    }
}

static void ToggleUp()
{
    gpio_pad_select_gpio(button);
    gpio_set_direction(button, GPIO_MODE_INPUT);
	while(1){
    if (gpio_get_level(button) == 1 && buttonPressed == false){
      buttonPressed = true;
      if (direction == 1) direction = 0;
      else if (direction == 0) direction = 1;
    }
    else if (gpio_get_level(button) == 0){
      buttonPressed = false;
    }
  vTaskDelay(50 / portTICK_PERIOD_MS);
	}
}



static void i2c_example_master_init(){
    // Debug
    printf("\n>> i2c Config\n");
    int err;

    // Port configuration
    int i2c_master_port = I2C_EXAMPLE_MASTER_NUM;

    /// Define I2C configurations
    i2c_config_t conf;
    conf.mode = I2C_MODE_MASTER;                              // Master mode
    conf.sda_io_num = I2C_EXAMPLE_MASTER_SDA_IO;              // Default SDA pin
    conf.sda_pullup_en = GPIO_PULLUP_ENABLE;                  // Internal pullup
    conf.scl_io_num = I2C_EXAMPLE_MASTER_SCL_IO;              // Default SCL pin
    conf.scl_pullup_en = GPIO_PULLUP_ENABLE;                  // Internal pullup
    conf.master.clk_speed = I2C_EXAMPLE_MASTER_FREQ_HZ;       // CLK frequency
    err = i2c_param_config(i2c_master_port, &conf);           // Configure
    if (err == ESP_OK) {printf("- parameters: ok\n");}

    // Install I2C driver
    err = i2c_driver_install(i2c_master_port, conf.mode,
                       I2C_EXAMPLE_MASTER_RX_BUF_DISABLE,
                       I2C_EXAMPLE_MASTER_TX_BUF_DISABLE, 0);
    // i2c_set_data_mode(i2c_master_port,I2C_DATA_MODE_LSB_FIRST,I2C_DATA_MODE_LSB_FIRST);
    if (err == ESP_OK) {printf("- initialized: yes\n\n");}

    // Dat in MSB mode
    i2c_set_data_mode(i2c_master_port, I2C_DATA_MODE_MSB_FIRST, I2C_DATA_MODE_MSB_FIRST);
}

